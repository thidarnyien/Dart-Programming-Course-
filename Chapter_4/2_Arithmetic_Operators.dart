//Arithmetic Operator

void main() {
  int num1 = 20;
  int num2 = 40;
  int sum = num1 + num2; //+ additional operator
  int reduce = num2 - num1; //- reducing operator
  int multi = num1 * num2; // multiple operator
  double divided = (num2 / num1);
  int divided2 = (num2 ~/ num1);
  int module = num2 % num1;
  int increase = ++num1;
  int decrease = --num1;

  print(sum);
  print(reduce);
  print(multi);
  print(divided);
  print(divided2);
  print(module);
  print(increase);
  print(decrease);
}
